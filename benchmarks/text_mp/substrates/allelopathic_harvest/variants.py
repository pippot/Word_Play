from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True)
class AllelopathicHarvestVariant:
    name: str
    title: str
    tilemap: str
    default_agent_count: int
    observation_radius: int
    open_patch_mode: bool

    @property
    def model_key(self) -> str:
        return "allelopathic_harvest" if self.name == "standard" else f"allelopathic_harvest__{self.name}"

    @property
    def description(self) -> str:
        return f"Allelopathic Harvest: {self.title}, adapted from Melting Pot."

    @property
    def prompt(self) -> str:
        if self.open_patch_mode:
            return (
                "Eat ripe berries by moving onto them. Plant red, green, or blue berries on empty patches. "
                "Use zaps when they improve your score."
            )
        return (
            "Move between berry patches, take berries from patches into your inventory, repaint patches, "
            "and zap opponents when it helps your score."
        )


